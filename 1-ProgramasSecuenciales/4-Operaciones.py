# 4. Dados dos números, mostrar la suma, resta, división y multiplicación de ambos.

num1 = float(input("Indica el primer número: "))        # Preguntamos los dos números
num2 = float(input("Indica el segundo número: "))

print("La suma de los dos números es: ", num1 + num2)       # Imprimimos los resultados de las diferentes operaciones
print("La resta de los dos números es: ", num1 - num2)
print("La división de los dos números es: ", num1 / num2)
print("La multiplicación de los dos números es: ", num1 * num2)
